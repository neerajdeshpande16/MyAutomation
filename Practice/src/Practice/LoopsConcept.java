package Practice;

public class LoopsConcept {

	public static void main(String[] args) {

		//1. While Loop:
		
		int i =1;
		while(i<=10){
			System.out.println(i);
			i=i+1;
		}
		
		System.out.println("**********");
		
		//2. For loop:
		
		for(int j=0; j<10; j++){ //Initialization, Condtional, Increment/Decrement
			System.out.println(j);
		}
		
		
	}

}
